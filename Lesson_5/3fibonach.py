def fibonacci(x):
    # Инициализируем первые два числа последовательности Фибоначчи
    a, b = 1, 1

    # Пока текущее число не превышает заданное значение x
    while a <= x:
        # Выводим текущее число, не переходя на новую строку
        print(a, end=" ")

        # Обновляем значения:
        # a принимает значение b (следующее число),
        # b становится суммой предыдущих a и b
        a, b = b, a + b

# Запрашиваем у пользователя максимальное число для последовательности
x = int(input("Введите максимальное число Фибоначчи: "))

# Вызываем функцию для печати чисел Фибоначчи
fibonacci(x)
