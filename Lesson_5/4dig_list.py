# Создаём список чисел  
x = [1, 2, 3, 4, 5, 6, 7, 8, 9, 19, 22, -1, 0]  

# Вычисляем сумму всех чисел в списке  
b = sum(x)  

# Находим минимальное значение в списке  
mini = min(x)  

# Находим максимальное значение в списке  
maxi = max(x)  

# Выводим результаты  
print(f"Сумма всех чисел списка: {b}")  
print(f"Максимальное число списка: {maxi}")  
print(f"Минимальное число списка: {mini}")  
