#Реализовать программу для вывода
#последовательности чисел Фибоначчи до определённого
#числа в последовательности. Номер числа, до которого нужно
#выводить, задаётся пользователем с клавиатуры. Для
#реализации последовательности использовать генераторную
#функцию.


def fibonacci(n):
    """Генераторная функция для последовательности Фибоначчи"""
    a, b = 0, 1  # Первые два числа
    for _ in range(n):  # Генерируем n чисел
        yield a  # Отдаем текущее число
        a, b = b, a + b  # Обновляем значения a и b

# Запрос числа у пользователя
n = int(input("Введите номер числа Фибоначчи: "))

# Выводим последовательность с помощью генератора
print("Последовательность Фибоначчи:")
for num in fibonacci(n):
    print(num, end=" ")